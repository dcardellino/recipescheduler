export function classifyIngredient(name: string): IngredientCategoryValue {
  if (!name) return "andere";
  const needle = name.toLocaleLowerCase("de-DE").trim();
  if (!needle) return "andere";
  for (const [category, keywords] of KEYWORDS) {
    for (const keyword of keywords) {
      if (needle.includes(keyword)) {
        return category;
      }
    }
  }
  return "andere";
}
